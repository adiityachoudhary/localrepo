#for loop 
#range function
#range(0,4) is similar to range(4)
#it will goes from 0 to n-1


for i in range(0,4):
    print(i)


#LIST iteration
li=["list", 22, 55, "Harry", "IIT", "KGP"]

for item in li:
    print(item)

#TUPLE iteration
tup=(2,3,4,"Harry", True)

for item in tup:
    print(item)

#STRING iteration
str="Harry"
for char in str:
    print(char)