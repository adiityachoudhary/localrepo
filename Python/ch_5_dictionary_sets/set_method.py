s={4,3,5,6,75,"Harry"}

print("Length of set s : ", len(s))

# s.add() will add the element in the set s
s.add(233) 
print(s)


# s.remove(4) remove the element 4 from the set
s.remove(4)
print(s)

# s.pop() removes a random element from the set
s.pop()  #everytimes it deletes a random element
print(s)

# s.clear() removes all the element fromt the set
s.clear()
print(s)
